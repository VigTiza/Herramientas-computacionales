#------------------------------------------------------------------------------------------------------------------

import cv2

cam_port = 1
cam = cv2.VideoCapture(cam_port)

while True:

    result, frame = cam.read()    
    
    if result: 

        cv2.imshow("Press q to quit", frame)

        if cv2.waitKey(1) & 0xFF == ord('q'):
            break

    else:
        print("No image detected")
        break

cam.release()
cv2.destroyAllWindows()

